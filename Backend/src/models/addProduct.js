import mongoose from "mongoose";
const addproduct = new mongoose.Schema({
    name: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true
    },
    image: {
        type: String,
        required: true
    }
})

const add = mongoose.model("addproduct", addproduct);
export default add;